package ex_002JavaBasics;

public class Lab_019_Constants {
    public static void main(String[] args) {
        int a=15;
        a=10;
        System.out.println(a);
        final int b=12;
        final float f=10.1f;
        final double d =10.55;
        System.out.println(b);
        System.out.println(f);
        System.out.println(d);
    }
}
