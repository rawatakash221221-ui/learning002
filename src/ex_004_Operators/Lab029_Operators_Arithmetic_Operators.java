package ex_004_Operators;

public class Lab029_Operators_Arithmetic_Operators {
    public static void main(String[] args) {
        int a=10;
        int b=1;
        float c=3.0f;
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a*c);
        System.out.println(a%b);
        System.out.println(a%c);


    }
}
