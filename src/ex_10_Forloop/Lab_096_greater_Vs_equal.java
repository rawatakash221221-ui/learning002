package ex_10_Forloop;

public class Lab_096_greater_Vs_equal {
    public static void main(String[] args) {
        for (int i = 0; i <10 ; i++) {
            System.out.println(i);

        }
        System.out.println();
        for (int h = 1; h <=10 ; h++) {
            System.out.println(h);

        }
        System.out.println();
        for (int k = 0; k <=10; k++) {
            System.out.println(k);

        }
    }
}
